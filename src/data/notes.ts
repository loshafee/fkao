// ---- 类型定义 ----

export interface ExamPoint {
  name: string;
  stars: number;        // 1-5
  frequency?: string;   // "5/5"
  questionType?: string[];
  sectionRef: string;
}

export interface SubTable {
  headers: string[];
  rows: string[][];
}

export interface ConceptSection {
  title: string;
  definition: string;
  keywords: string[];
  examTips: string;
  points: string[];
  classicCase?: string;
  subTable?: SubTable;
}

export interface ComparisonRow {
  dimA: string;
  dimB: string;
  dimC?: string;
  keyDiff: string;
  oneLiner?: string;
}

export interface ComparisonTable {
  title: string;
  headers: string[];
  rows: ComparisonRow[];
}

export interface MnemonicItem {
  topic: string;
  rhyme: string;
  meaning: string;
}

export interface TrapItem {
  trap: string;
  wrong: string;
  correct: string;
}

export interface KeyNumber {
  number: string;
  meaning: string;
}

export interface Update2026Item {
  content: string;
  keyPoints: string;
  priority: 'red' | 'yellow' | 'green';
}

export type NoteModule = 'examPoints' | 'concepts' | 'comparisons' | 'mnemonics' | 'traps' | 'keyNumbers' | 'updates2026';

export interface SubjectNote {
  subjectId: string;
  subjectName: string;
  subjectIcon: string;
  tier: 'T0' | 'T1' | 'T2' | 'T3';
  readTime: string;
  meta: {
    scoreWeight: string;
    studyHours: string;
    change2026: string;
  };
  examPoints: ExamPoint[];
  concepts: ConceptSection[];
  comparisons: ComparisonTable[];
  mnemonics: MnemonicItem[];
  traps: TrapItem[];
  keyNumbers: KeyNumber[];
  updates2026: Update2026Item[];
}

